package com.hhu.model;

public class WaterPressureData {
    private String time;
    private String internal_number;
    private String water_pressure;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getInternal_number() {
        return internal_number;
    }

    public void setInternal_number(String internal_number) {
        this.internal_number = internal_number;
    }

    public String getWater_pressure() {
        return water_pressure;
    }

    public void setWater_pressure(String water_pressure) {
        this.water_pressure = water_pressure;
    }
}
