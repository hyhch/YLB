package com.hhu.model;

public class AccelerationData {
    private String time;
    private String internal_number;
    private String acceleration;
    private String total_acceleration;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getInternal_number() {
        return internal_number;
    }

    public void setInternal_number(String internal_number) {
        this.internal_number = internal_number;
    }

    public String getAcceleration() {
        return acceleration;
    }

    public void setAcceleration(String acceleration) {
        this.acceleration = acceleration;
    }

    public String getTotal_acceleration() {
        return total_acceleration;
    }

    public void setTotal_acceleration(String total_acceleration) {
        this.total_acceleration = total_acceleration;
    }
}
