package com.hhu.model;

public class Diagram {
    private Integer trans_x;
    private Integer trans_y;
    private Integer density;

    public Integer getTrans_x() {
        return trans_x;
    }

    public void setTrans_x(Integer trans_x) {
        this.trans_x = trans_x;
    }

    public Integer getTrans_y() {
        return trans_y;
    }

    public void setTrans_y(Integer trans_y) {
        this.trans_y = trans_y;
    }

    public Integer getDensity() {
        return density;
    }

    public void setDensity(Integer density) {
        this.density = density;
    }
}
