package com.hhu.model;

public class OriginalPhoto {
    private String time;
    private String internal_number;
    private String shovel_number;
    private String volume;
    private String total_volume;
    private String longitude;
    private String latitude;
    private String altitude;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getInternal_number() {
        return internal_number;
    }

    public void setInternal_number(String internal_number) {
        this.internal_number = internal_number;
    }

    public String getShovel_number() {
        return shovel_number;
    }

    public void setShovel_number(String shovel_number) {
        this.shovel_number = shovel_number;
    }

    public String getVolume() {
        return volume;
    }

    public void setVolume(String volume) {
        this.volume = volume;
    }

    public String getTotal_volume() {
        return total_volume;
    }

    public void setTotal_volume(String total_volume) {
        this.total_volume = total_volume;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getAltitude() {
        return altitude;
    }

    public void setAltitude(String altitude) {
        this.altitude = altitude;
    }
}
